# インテラブるなオブジェクトからのタプルの作成
# ベルのタプルからタプルを作成する
mytuple = ("A", "B", "C")
newtuple = tuple(mytuple);
print(newtuple)

# 文字列からタプルを作成する
mytuple = tuple("Hello Python")
print(mytuple)

#椅子とからタプルを取得する
mytuple = tuple(["a", "B", "C"])
print(mytuple)



# rangeからタプルを取得する
mytuple = tuple(range(10))
print(mytuple)